# ── Helper: set thai font on a style's rPr ──
def style_set_font(style, name=FONT, size=Pt(16), bold=False, color=None):
    style.font.name = name
    style.font.size = size
    style.font.bold = bold
    if color:
        style.font.color.rgb = color
    # Thai font via XML
    rPr = style.element.find(qn('w:rPr'))
    if rPr is None:
        rPr = parse_xml(f'<w:rPr {nsdecls("w")}/>')
        style.element.append(rPr)
    rFonts = rPr.find(qn('w:rFonts'))
    if rFonts is None:
        rFonts = parse_xml(f'<w:rFonts {nsdecls("w")} w:ascii="{name}" w:hAnsi="{name}" w:cs="{name}" w:thai="{name}"/>')
        rPr.insert(0, rFonts)
    else:
        for attr in ('w:ascii', 'w:hAnsi', 'w:cs', 'w:thai'):
            rFonts.set(qn(attr), name)

def style_set_spacing(style, before=0, after=0, line=1.15):
    pf = style.paragraph_format
    pf.space_before = Pt(before)
    pf.space_after = Pt(after)
    pf.line_spacing = line
    pf.line_spacing_rule = WD_LINE_SPACING.MULTIPLE

