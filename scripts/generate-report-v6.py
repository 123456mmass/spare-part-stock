from docx.oxml import parse_xml
from lxml import etree
import os, copy

BASE = os.path.dirname(os.path.dirname(os.path.abspath(__file__)))
IMG_DIR = os.path.join(BASE, "docs", "images")
TEMPLATE = os.path.join(BASE, "docs", "ระบบจัดการสต๊อกอะไหล่.docx")
OUT_PATH = os.path.join(BASE, "docs", "report-v6-final.docx")

FONT = 'TH SarabunPSK'

# ── Open template ──
doc = Document(TEMPLATE)
body = doc.element.body

def _get_sectPr():
	sp = body.find(qn('w:sectPr'))
	return sp

def _insert_before_sectPr(elem):
	sp = _get_sectPr()
	if sp is not None:
		sp.addprevious(elem)
	else:
		body.append(elem)

REF_CHAPTER = doc.paragraphs[62]._element
REF_SECTION = doc.paragraphs[63]._element
REF_BODY = doc.paragraphs[64]._element
REF_CENTER = doc.paragraphs[2]._element

def _make_para_from_ref(ref_elem, text, bold=None):
	new_p = copy.deepcopy(ref_elem)
	for r in new_p.findall(qn('w:r')):
		new_p.remove(r)
	ref_runs = ref_elem.findall(qn('w:r'))
	new_r = parse_xml(f'<w:r {nsdecls("w")}></w:r>')
	if ref_runs:
		ref_rPr = ref_runs[0].find(qn('w:rPr'))
		if ref_rPr is not None:
			new_rPr = copy.deepcopy(ref_rPr)
			if bold is True:
				b_elem = new_rPr.find(qn('w:b'))
				if b_elem is None:
					b_elem = parse_xml(f'<w:b {nsdecls("w")}/>')
					new_rPr.insert(0, b_elem)
				else:
					b_elem.set(qn('w:val'), '1')
				bCs = new_rPr.find(qn('w:bCs'))
				if bCs is None:
					bCs = parse_xml(f'<w:bCs {nsdecls("w")}/>')
					new_rPr.append(bCs)
				else:
					bCs.set(qn('w:val'), '1')
			elif bold is False:
				b_elem = new_rPr.find(qn('w:b'))
				if b_elem is not None:
					b_elem.set(qn('w:val'), '0')
				bCs = new_rPr.find(qn('w:bCs'))
				if bCs is not None:
					bCs.set(qn('w:val'), '0')
			new_r.append(new_rPr)
	t = parse_xml(f'<w:t {nsdecls("w")} xml:space="preserve">{_escape_xml(text)}</w:t>')
	new_r.append(t)
	new_p.append(new_r)
	_insert_before_sectPr(new_p)
	return new_p

def _escape_xml(text):
	return text.replace('&', '&amp;').replace('<', '&lt;').replace('>', '&gt;').replace('"', '&quot;')

def _add_page_break():
	pb = parse_xml(
		f'<w:p {nsdecls("w")}>'
		f'  <w:r><w:br w:type="page"/></w:r>'
		f'</w:p>'
	)
	_insert_before_sectPr(pb)

def make_chapter(text):
	return _make_para_from_ref(REF_CHAPTER, text, bold=True)

def make_section(text):
	return _make_para_from_ref(REF_SECTION, text, bold=True)

def make_subsection(text):
	return _make_para_from_ref(REF_BODY, text, bold=False)

def make_body(text):
	return _make_para_from_ref(REF_BODY, text, bold=False)

def make_bullet(text):
	return _make_para_from_ref(REF_BODY, f"• {text}", bold=False)

def make_empty():
	return _make_para_from_ref(REF_BODY, "", bold=False)

def make_placeholder(caption_text):
	caption = f"[{caption_text}]"
	cap_p = _make_para_from_ref(REF_BODY, caption, bold=False)
	for r in cap_p.findall(qn('w:r')):
		rPr = r.find(qn('w:rPr'))
		if rPr is not None:
			rPr.append(parse_xml(f'<w:i {nsdecls("w")}/>'))
			rPr.append(parse_xml(f'<w:iCs {nsdecls("w")}/>'))
			sz = rPr.find(qn('w:sz'))
			if sz is not None:
				sz.set(qn('w:val'), '28')
			szCs = rPr.find(qn('w:szCs'))
			if szCs is not None:
				szCs.set(qn('w:val'), '28')
	cpPr = cap_p.find(qn('w:pPr'))
	if cpPr is None:
		cpPr = parse_xml(f'<w:pPr {nsdecls("w")}/>')
		cap_p.insert(0, cpPr)
	cjc = cpPr.find(qn('w:jc'))
	if cjc is None:
		cpPr.append(parse_xml(f'<w:jc {nsdecls("w")} w:val="center"/>'))
	else:
		cjc.set(qn('w:val'), 'center')
	return cap_p

def add_image(filename, caption="", width=Inches(5.2)):
	if filename.startswith("mobile-app/"):
		path = os.path.join(BASE, "docs", "mobile app image", filename.split("/", 1)[1])
	else:
		path = os.path.join(IMG_DIR, filename)
	p = doc.add_paragraph()
	p.alignment = WD_ALIGN_PARAGRAPH.CENTER
	run = p.add_run()
	if os.path.exists(path):
		run.add_picture(path, width=width)
	else:
		run.text = f"[รูปภาพ: {filename}]"
	p_elem = p._element
	p_elem.getparent().remove(p_elem)
	_insert_before_sectPr(p_elem)
	if caption:
		cap_p = _make_para_from_ref(REF_BODY, caption, bold=False)
		for r in cap_p.findall(qn('w:r')):
			rPr = r.find(qn('w:rPr'))
			if rPr is not None:
				rPr.append(parse_xml(f'<w:i {nsdecls("w")}/>'))
				rPr.append(parse_xml(f'<w:iCs {nsdecls("w")}/>'))
				sz = rPr.find(qn('w:sz'))
				if sz is not None:
					sz.set(qn('w:val'), '28')
				szCs = rPr.find(qn('w:szCs'))
				if szCs is not None:
					szCs.set(qn('w:val'), '28')
		cpPr = cap_p.find(qn('w:pPr'))
		if cpPr is None:
			cpPr = parse_xml(f'<w:pPr {nsdecls("w")}/>')
			cap_p.insert(0, cpPr)
		cjc = cpPr.find(qn('w:jc'))
		if cjc is None:
			cpPr.append(parse_xml(f'<w:jc {nsdecls("w")} w:val="center"/>'))
		else:
			cjc.set(qn('w:val'), 'center')

def add_table(headers, rows):
	t = doc.add_table(rows=1+len(rows), cols=len(headers))
	t.alignment = WD_TABLE_ALIGNMENT.CENTER
	tbl = t._tbl
	tblPr = tbl.find(qn('w:tblPr'))
	if tblPr is None:
		tblPr = parse_xml(f'<w:tblPr {nsdecls("w")}/>')
		tbl.insert(0, tblPr)
	borders = parse_xml(
		f'<w:tblBorders {nsdecls("w")}>'
		'  <w:top w:val="single" w:sz="4" w:space="0" w:color="auto"/>'
		'  <w:left w:val="single" w:sz="4" w:space="0" w:color="auto"/>'
		'  <w:bottom w:val="single" w:sz="4" w:space="0" w:color="auto"/>'
		'  <w:right w:val="single" w:sz="4" w:space="0" w:color="auto"/>'
		'  <w:insideH w:val="single" w:sz="4" w:space="0" w:color="auto"/>'
		'  <w:insideV w:val="single" w:sz="4" w:space="0" w:color="auto"/>'
		'</w:tblBorders>'
	)
	existing = tblPr.find(qn('w:tblBorders'))
	if existing is not None:
		tblPr.remove(existing)
	tblPr.append(borders)
	for i, h in enumerate(headers):
		c = t.rows[0].cells[i]
		c.text = h
		for p in c.paragraphs:
			p.alignment = WD_ALIGN_PARAGRAPH.CENTER
			for r in p.runs:
				r.bold = True
				r.font.name = FONT
				r.font.size = Pt(18)
	for ri, row in enumerate(rows):
		for ci, val in enumerate(row):
			cell = t.rows[ri+1].cells[ci]
			cell.text = str(val)
			for p in cell.paragraphs:
				for r in p.runs:
					r.font.name = FONT
					r.font.size = Pt(18)
	tbl_elem = t._tbl
	tbl_elem.getparent().remove(tbl_elem)
	_insert_before_sectPr(tbl_elem)


