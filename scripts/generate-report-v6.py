"""
Generate internship report v6 — template-based, insert-before-sectPr approach.
All content is inserted BEFORE the <w:sectPr> element so nothing
ends up on the wrong page. Paragraphs are cloned from the original
to preserve exact formatting (fonts, spacing, bold, cs, szCs, etc).
"""
from docx import Document
from docx.shared import Inches, Pt, Cm, Emu
from docx.enum.text import WD_ALIGN_PARAGRAPH, WD_LINE_SPACING
from docx.enum.table import WD_TABLE_ALIGNMENT
